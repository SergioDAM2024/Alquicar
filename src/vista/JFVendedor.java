    private void agregar() {
        int codigo = Integer.parseInt(txtCodigo.getText());
        boolean validar = txtCodigo.getText().equals("") || txtNombre.getText().equals("") || txtApePat.getText().equals("")
                || txtApeMat.getText().equals("") || txtEdad.getText().equals("") || txtTel.getText().equals("")
                || cboSexo.getSelectedIndex() == 0;
        for (int i = 0; i < lista.cantidadVendedor(); i++) {
            if (lista.obtener(i).getCodigoVendedor() == codigo) {
                JOptionPane.showMessageDialog(null, "El código ya existe");
                codigo = -1;
                txtCodigo.requestFocus();
                i = lista.cantidadVendedor();
            }

        }
        if (!validar && codigo != -1) {

            try {
                Vendedor emp = new Vendedor(getCodigo(), getNombre(), getApellidoPat(), getApellidoMat(), getEdad(), getTelefono(), getSexo());
                lista.agregar(emp);
                lista.grabar();
                JOptionPane.showMessageDialog(null, "Se agrego correctamente");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar" + e.toString());
            }
        } else if (codigo == -1) {

        } else {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
        }

    }

